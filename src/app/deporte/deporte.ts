/**
 * Clase que represenanta un deporte.
 */
export class Deporte {
  /**
  * Id del deporte
  */
  id: number;

  /**
   * Nombre del deporte.
   */
  nombre: string;

  /**
   * Ubicación de la imagen del deporte.
   */
  imagen: string;

  /**
   * Descripción breve del deporte.
   */
  descripcion: string;
}
