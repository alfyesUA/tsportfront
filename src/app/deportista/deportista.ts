/**
 * Clase que represenanta un deporte.
 */
export class Deportista {
  /**
  * Id del deportista
  */
  id: number;

  /**
   * Nombre del deportista.
   */
  nombre: string;

  /**
   * Ubicación de la imagen del deportista.
   */
  imagen: string;

  /**
   * Biografía breve del deportista.
   */
  bio: string;

  /**
   * Fecha de nacimiento del deportista.
   */
  fechaNacimiento: string;
}
